    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        Account result =  accountDirectory.searchAccount(txtAccountNumber.getText());
        if(result == null){
             JOptionPane.showMessageDialog(null, "Account number you entered doesn't exist!!!", "Warning", JOptionPane.WARNING_MESSAGE); 
        }else{
             ViewJPanel panel = new ViewJPanel(containerJPanel, result);
            containerJPanel.add("ManagementAccountJPanel", panel);
            CardLayout layout = (CardLayout)containerJPanel.getLayout();
            layout.next(containerJPanel);
            
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tableAccount.getSelectedRow();
        if(selectedRow>=0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the confirm details???",
                    "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Account account = (Account) tableAccount.getValueAt(selectedRow, 0);
                accountDirectory.deleteAccount(account);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please selected a row from table first", 
                    "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableAccount.getSelectedRow();
        if(selectedRow<0){
             JOptionPane.showMessageDialog(null, "Please selected a row from table first", "Warning", JOptionPane.WARNING_MESSAGE); 
        }
        else{
            Account account = (Account) tableAccount.getValueAt(selectedRow, 0);
            ViewJPanel panel = new ViewJPanel(containerJPanel, account);
            containerJPanel.add("ManagementAccountJPanel", panel);
            CardLayout layout = (CardLayout)containerJPanel.getLayout();
            layout.next(containerJPanel);
        }
    }//GEN-LAST:event_btnViewActionPerformed

