    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        try{
        Flight flight;
        int flightId = Integer.parseInt(idField.getText());
        
       
        flight = travelAgency.flightDirectory.searchFlight(flightId);
        ViewFlightDetailsJPanel vpdjp = new ViewFlightDetailsJPanel(userProcessContainer, customer,flight);
        userProcessContainer.add("ViewFlightDetailsJPanel", vpdjp);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }catch (NumberFormatException e) {
            //an exception is thrown when parse statement executes if 
             //user enters an invalid value.
            JOptionPane.showMessageDialog(null,"Only Numbers allowed");
        }

    }//GEN-LAST:event_searchButtonActionPerformed

