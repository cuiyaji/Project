    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) jtbVitalSigns.getModel();
        dtm.setRowCount(0);
        
        for(VitalSigns vs : vsh.getVitalSignsHistory()){
            Object[] row = new Object[2];
            row[0] = vs;
            row[1] = vs.getBlood();
            dtm.addRow(row);
            
        }
    }
    
