    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = jtbVitalSigns.getSelectedRow();
        if(selectedRow >= 0){
            
            VitalSigns vs = (VitalSigns)jtbVitalSigns.getValueAt(selectedRow, 0);
            jTFBlood.setText(String.valueOf(vs.getBlood()));
            jTFDate.setText(vs.getDate());
            jTFPulse.setText(String.valueOf(vs.getPulse()));
            jTFTemp.setText(String.valueOf(vs.getTemperature()));
            
                    
            
        }else{
            JOptionPane.showMessageDialog(null, "Please selected any row!!!","Alert", JOptionPane.ERROR);
        }
            
        
    }//GEN-LAST:event_btnViewDetailsActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = jtbVitalSigns.getSelectedRow();
        if(selectedRow >= 0){
            int dialogRes = JOptionPane.showConfirmDialog(null, "This is Vital Sign, Do you want to delete it?");
            if(dialogRes == JOptionPane.YES_OPTION){
               VitalSigns vs = (VitalSigns)jtbVitalSigns.getValueAt(selectedRow, 0);
               vsh.deleteVitals(vs);
               JOptionPane.showMessageDialog(null, "You have delete this  Vital Sign!!");
               populateTable();
               restTextField();
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Please selected any row!!!","Alert", JOptionPane.ERROR);
        }
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed


